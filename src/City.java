public class City {
    private String name;

    public City(String name) {
        this.name = name;
    }

    /**
     * Gets the name of the city.
     * @return name
     */
    public String getName() {
        return name;
    }

    /**
     * Sets the name of the city.
     * @param name of the city
     */
    public void setName(String name) {
        this.name = name;
    }
}
